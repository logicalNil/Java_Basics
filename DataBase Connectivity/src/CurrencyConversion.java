public class CurrencyConversion {
    public static double dollarToRupee(double dollar) {
        return dollar * 74.3;
    }

    public static double rupeeToEuro(double rupee) {
        return rupee / 88.6;
    }

    public static double rupeeToYen(double rupee) {
        return rupee / 0.67;
    }
}
